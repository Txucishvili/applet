function CustomLink({ children, to, ...props }: LinkProps) {
  let resolved = useResolvedPath(to);
  let match = useMatch({ path: resolved.pathname, end: true });

  return (
    <div>
      <Link
        className={match ? 'active' : ''}
        to={to}
        {...props}
      >
        {children}
      </Link>
    </div>
  );
}



